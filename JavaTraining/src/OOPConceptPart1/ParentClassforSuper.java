package OOPConceptPart1;

public class ParentClassforSuper {

	public ParentClassforSuper() {

		System.out.println("parent class constr");
	}

	public ParentClassforSuper(int i) {

		System.out.println("single param parent class constr " + i);
	}

	public ParentClassforSuper(int i, int j) {

		System.out.println("parent class constr with value of i is " + i);
		System.out.println("parent class constr with value of j is " + j);

	}

}
